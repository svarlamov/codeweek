<p><strong>Podaci o ličnosti</strong></p>

<p>Možemo prikupljati podatke o ličnosti Korisnika na vi&scaron;e načina uključujući, bez ograničenja, i posjetu Korisnika na&scaron;oj stranici, registraciju na stranici, kao i one u vezi sa drugim aktivnostima, uslugama, člancima i materijalima dostupnim na na&scaron;oj Stranici. Od Korisnika se, prema potrebi, može zatražiti ime i prezime, email adresa. Međutim, Korisnik može na&scaron;u Stranicu posjetiti anonimno. Mi ćemo lične podatkeKorisnika prikupljati samo ukoliko su dati na dobrovoljnoj bazi. Korisnik ima prava odbiti da daje lične podatke, ali mu takva radnja može ograničiti pristup određenim aktivnostima u vezi sa Stranicom.</p>

<p><strong>Podaci koji nijesu lične prirode</strong></p>

<p>Možemo prikupljati podatke o Korisnicima koji nijesu lične prirode uvijek kada pristupe na&scaron;oj Stranici. Podaci koji nijesu lične prirode mogu podrazumijevati i naziv pretraživača, tip računara i tehničke podatke o načinu pristupa Korisnika na&scaron;oj Stranici, poput operativnog sistema i kori&scaron;ćenog Internet provajdera i druge, slične informacije.</p>

