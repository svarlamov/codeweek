			<p>Ovom Politikom privatnosti uređuje se način na koji Evropska nedelja programiranja prikuplja, koristi, održava i otkriva informacije prikupljene od korisnika (u daljem tekstu &bdquo;Korisnik&ldquo;) na veb-sajtu <a href="http://codeweek.eu">http://codeweek.eu</a> (&bdquo;Sajt&ldquo;). Ova Politika privatnosti se odnosi na Sajt i sve proizvode i usluge koje nudi Evropska nedelja programiranja.</p>

<p><strong>Lični identifikacioni podaci</strong></p>

<p>Možemo prikupiti lične identifikacione podatke od korisnika na različite načine, uključujući, ali ne ograničavajući se na, kada Korisnici posećuju na&scaron; sajt, registruju se na sajtu i u vezi sa drugim aktivnostima, uslugama, funkcijama ili resursima dostupnim na na&scaron;em Sajtu. Korisnicima se može tražiti, po potrebi, ime, imejl adresa. Međutim, korisnici mogu anonimno posetiti na&scaron; Sajt. Prikupljamo lične identifikacione podatke od Korisnika samo ako oni dobrovoljno dostave takve podatke nama. Korisnici mogu uvek da odbiju da pruže lične identifikacione podatke, jedino &scaron;to ih to može sprečiti da se angažuju u određenim aktivnostima u vezi sa Sajtom.</p>

<p><strong>Podaci ne mogu lično da identifikuju osobu</strong></p>

<p>Možemo prikupiti podatke koji ne mogu lično da identifikuju osobu o Korisnicima kad god su u interakciji sa na&scaron;im Sajtom. Podaci koji ne mogu lično da identifikuju mogu da obuhvataju naziv pretraživača, vrstu računara i tehničke informacije o tome kako se Korisnici povezuju na na&scaron; Sajt, kao &scaron;to su operativni sistem, internet provajder i druge slične informacije.</p>

<p><strong>Kolačići veb-pregledača</strong></p>

<p>Na&scaron; Sajt može da koristi &bdquo;kolačiće&ldquo; kako bi pobolj&scaron;ao korisničko iskustvo. Korisnikov pretraživač stavlja kolačiće na hard disk radi vođenja evidencije i ponekad prati informacije o njima. Korisnik može da izabere da podesi svoj pretraživač da odbije kolačiće ili da primi upozorenje kada se &scaron;alju kolačići. Ako to učini, treba imati na umu da neki delovi Sajta možda neće funkcionisati pravilno.</p>

<p><strong>Kako koristimo informacije koje prikupljamo</strong></p>

<p>Evropska nedelja programiranja može da prikuplja i koristi lične podatke Korisnika u sledeće svrhe:</p>
<ul>
<li><i>Da pobolj&scaron;a uslugu korisnicima</i>: Informacije koje nam pružite pomažu nam da efikasnije odgovorimo na va&scaron;e zahteve i potrebe.</li>
<li><i>Da personalizuje iskustvo korisnika</i>: Možemo da koristimo informacije u celini kako bismo razumeli kako Korisnici kao grupa koriste usluge i resurse sa na&scaron;eg Sajta.</li>
<li><i>Da &scaron;alje periodične imejlove</i>: Možemo da koristimo imejl adresu da odgovorimo na upite, pitanja i/ili druge zahteve korisnika.</li>
</ul>
<p><strong>Kako &scaron;titimo va&scaron;e podatke</strong></p>

<p>Mi usvajamo odgovarajući način prikupljanja, skladi&scaron;tenja i obrade podataka kao i mere za&scaron;tite protiv neovla&scaron;ćenog pristupa, izmene, obelodanjivanja ili uni&scaron;tavanja va&scaron;ih ličnih podataka, korisničkog imena, lozinke, informacija o transakciji i podataka koji se čuvaju na na&scaron;em Sajtu.</p>

<p><strong>Deljenje va&scaron;ih ličnih podataka</strong></p>

<p>Mi ne prodajemo, ne trgujemo niti iznajmljujemo lične identifikacione podatke drugima. Možemo da podelimo generički agregirane demografske informacije koje nisu povezane ni sa jednim ličnim identifikacionim podacima o posetiocima i korisnicima sa na&scaron;im poslovnim partnerima, pouzdanim partnerima i ogla&scaron;avačima u gore opisane svrhe. Možemo da koristimo pružaoce usluga trećih strana da nam pomognu da vodimo na&scaron;e poslovanje i Sajt ili da upravljaju aktivnostima u na&scaron;e ime, kao &scaron;to su slanje biltena ili anketa. Mi možemo da podelimo va&scaron;e podatke sa tim trećim licima u te ograničene svrhe pod uslovom da ste nam dali dozvolu.</p>

<p><strong>Promene u ovoj Politici privatnosti</strong></p>

<p>Evropska nedelja privatnosti zadržava diskreciono pravo da ažurira ovu politiku privatnosti u bilo kom trenutku. Kada to učinimo, ažuriraćemo datum na dnu ove stranice. Preporučujemo Korisnicima da često proveravaju ovu stranicu kako bi bili pravovremeno obave&scaron;teni o nastalim promenama. Korisnik će na ovaj način ostati informisan o tome na koji način &scaron;titimo lične podatke koje prikupimo. Prihvatate i saglasni ste da je va&scaron;a odgovornost da periodično pregledate ovu politiku privatnosti i da ste svesni izmena.</p>

<p><strong>Va&scaron;e prihvatanje ovih uslova</strong></p>

